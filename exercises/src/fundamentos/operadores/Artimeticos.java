package fundamentos.operadores;

public class Artimeticos {
	public static void main(String[] args) {
		System.out.println(2 + 1);
		
		var x = 35.2;
		double y = 2.5;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		int a = 5;
		int b = 2;
		
		System.out.println(a - b);
		System.out.println(a + b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a / (double) b);
	
				
	}
}
